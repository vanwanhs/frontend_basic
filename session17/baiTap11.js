
console.log(Math.abs(-5.5));
console.log(Math.abs(10));
// hàm abs là hàm giá trị tuyệt đối nên -5.5 = 5.5 và 10 = 10
console.log(Math.ceil(4.4));
console.log(Math.ceil(5.8));
// hàm ceil là hàm làm tròn lên
console.log(Math.floor(5.7));
console.log(Math.floor(7.8));
// Hàm floor là làm tròn xuống
console.log(Math.round(10.1));
console.log(Math.round(10.7));
// Hàm round là hàm làm tròn nếu . nhỏ hơn 5 thì làm tròn xuống còn nếu lớn hơn 5 thì làm tròn lên


 

